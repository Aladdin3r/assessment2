import Head from "next/head";
import styles from "@/styles/Home.module.css";
import { useState } from "react";
import Header from "@/components/Header";
import Footer from "@/components/Footer";
import Image from "next/image";
import bgImg from '../public/sky.svg';
import rawan from '../public/rawan.svg';
import jena from '../public/jena.svg';
import rama from '../public/rama.svg';
import shukran from '../public/shukran.svg';
import mom from '../public/mom.svg';
import dad from '../public/dad.svg';
import cloud from '../public/cloud.svg';
import 'animate.css';


export default function Home() {
  
  // Family Members
  const classFilters = {
    // key : value
    1: 'Rawan',
    2: 'Shukran',
    4: 'Rama',
    8: 'Jena',
    16: 'Mom',
    32: 'Dad'
  }

  const [input, setInput] = useState("");
  const [animateCharacters, setAnimateCharacters] = useState(false);

  const getClassStyle = flagValue => (input & flagValue) > 0 ? {backgroundColor: 'var(--light-blue)'} : null;

  const handleInputChange = event => {
    const inputValue = event.target.value;
    setInput(inputValue);
    setAnimateCharacters(!!inputValue); // Set animateCharacters to true if inputValue is not empty
  };

  const getBobbleStyle = flagValue => animateCharacters && (input & flagValue) > 0 ? { animation: 'slideInFromLeft 1s forwards, fadeIn 1s forwards' } : null;

  return (
    <>
      <Head>
        <title>Aladdin Family Vacation</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/rawan.svg" />
        
      </Head>
      <Header />
      <main className={`${styles.main}`}>
        <div className={styles.bgImg}>
          <Image 
            src={bgImg} 
            alt="sky"
            layout="fill"
            fill={true}
            priority={true}
            objectFit="cover"
          />
        </div>

        <div className={styles.bitmask}>
          <input
            id="input"
            type="text"
            placeholder="Bitmask Number"
            value={input}
            onChange={handleInputChange}
          />
          <table>
            <tbody>
              {
                Object.keys(classFilters).map(item => (
                  <tr key={item} style={getClassStyle(item)}>
                    <td>{item}</td>
                    <td>{classFilters[item]}</td>
                  </tr>
                ))
              }
            </tbody>
          </table>
        </div>

        <div className={styles.container}>
          {/* cloud for characters to hide */}
          {/* <Image 
            src={cloud}
            className={`${styles.cloud} animate__animated animate__backInLeft`}
            id="cloud"
            alt="cloud"
          /> */}
          {/* characters */}
          <Image 
            src={rawan}
            className={`${styles.bobble} ${getBobbleStyle(2)} animate__animated animate__backInLeft`}
            alt="rawan"
          />
          <Image 
            src={jena}
            className={`${styles.bobble} ${getBobbleStyle(4)} animate__animated animate__backInLeft`}
            alt="jena"
          />
          <Image 
            src={rama}
            className={`${styles.bobble} ${getBobbleStyle(8)} animate__animated animate__backInLeft`}
            alt="rama"
          />
          <Image 
            src={shukran}
            className={`${styles.bobble} ${getBobbleStyle(16)} animate__animated animate__backInLeft`}
            alt="shukran"
          />
          <Image 
            src={mom}
            className={`${styles.bobble} ${getBobbleStyle(32)} animate__animated animate__backInLeft`}
            alt="mom"
          />
          <Image 
            src={dad}
            className={`${styles.bobble} ${getBobbleStyle(64)} animate__animated animate__backInLeft`}
            alt="dad"
          />
          
        </div>
      </main>
      <Footer />
    </>
  );
}
